
public class Personaje {
	// POSICION DEL JUGADOR
	protected int personajeX;
	protected int personajeY;
	// velocidad del jugador
	protected int velocidadPersonaje;

	public Personaje(int personajeX, int personajeY, int velocidadPersonaje) {
		super();
		this.personajeX = personajeX;
		this.personajeY = personajeY;
		this.velocidadPersonaje = velocidadPersonaje;
	}

}
